//Arrow functions as predicates
//Arrow functions are a more concise syntax for writing function expressions in JavaScript. They were introduced in ES6 (ECMAScript 2015) and have become popular due to their simplicity and readability. Arrow functions also have some unique features, such as lexical scoping of the `this` keyword.
// Arrow functions are defined using the `=>` syntax. They can be used in various contexts, such as in array methods, event handlers, and as callbacks.

//Finding Elements reference types (objects, arrays, functions)

function add(num1, num2){
    return num1 + num2;
}
//Arrow function
const add2 = (num1, num2) => num1 + num2;